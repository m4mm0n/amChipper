    /// <summary>
    /// Executes the Translate operation.
    /// </summary>
    public static string Translate(string language, string key)
    {
        string normalized = NormalizeLanguage(language);
        if (ExternalTranslations.Value.TryGetValue(normalized, out var external) &&
            external.TryGetValue(key, out string? externalValue))
        {
            return StripMnemonicMarkers(externalValue);
        }

        var dictionary = normalized switch
        {
            "Deutsch" => German,
            "Norsk" => Norwegian,
            "Svenska" => Swedish,
            "Dansk" => Danish,
            "Français" => French,
            "Español" => Spanish,
            "Italiano" => Italian,
            "Nederlands" => Dutch,
            "Polski" => Polish,
            "Português" => Portuguese,
            "Suomi" => Finnish,
            "Čeština" => Czech,
            "日本語" => Japanese,
            _ => English
        };

        string translated = dictionary.TryGetValue(key, out string? value)
            ? value
            : English.TryGetValue(key, out string? fallback) ? fallback : key;
        return StripMnemonicMarkers(translated);
    }

