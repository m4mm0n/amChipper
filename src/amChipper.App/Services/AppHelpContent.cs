    private static string SafeLanguageFileName(string language)
    {
        var invalid = Path.GetInvalidFileNameChars();
        string safe = new(language.Select(ch => invalid.Contains(ch) ? '_' : ch).ToArray());
        return safe.Replace(' ', '_');
    }

