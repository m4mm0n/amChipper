    public static void EnsureLanguageFiles(bool overwrite = false)
    {
        string directory = LanguageDirectory;
        Directory.CreateDirectory(directory);
        foreach (string language in BuiltInLanguages)
        {
            string path = Path.Combine(directory, $"{SafeLanguageFileName(language)}.json");
            var pack = new LanguagePack(language, language, GetBuiltInDictionary(language));
            if (File.Exists(path) && !overwrite)
            {
                if (MergeMissingLanguageEntries(path, pack))
                    continue;

                continue;
            }

            File.WriteAllText(path, JsonSerializer.Serialize(pack, JsonOptions));
        }

        string readme = Path.Combine(directory, "README.txt");
        if (!File.Exists(readme) || overwrite)
        {
            File.WriteAllText(readme,
                "amChipper language packs\r\n\r\n" +
                "Copy one of the JSON files, change language/nativeName, and translate values under entries.\r\n" +
                "Keep every key name unchanged. Restart amChipper after adding a new language file.\r\n" +
                "Use amChipper.Console lang-check --input lang to validate packs.\r\n");
        }
    }

    /// <summary>
    /// Exports one or all built-in language packs for translation work.
    /// </summary>
    public static void ExportLanguageFiles(string outputDirectory, string? language = null, bool overwrite = true)
    {
        Directory.CreateDirectory(outputDirectory);
        IEnumerable<string> languages = string.IsNullOrWhiteSpace(language)
            ? BuiltInLanguages
            : [NormalizeLanguage(language)];
        foreach (string item in languages)
        {
            var pack = new LanguagePack(item, item, GetBuiltInDictionary(item));
            string path = Path.Combine(outputDirectory, $"{SafeLanguageFileName(item)}.json");
            if (overwrite || !File.Exists(path))
                File.WriteAllText(path, JsonSerializer.Serialize(pack, JsonOptions));
        }
    }

    /// <summary>
    /// Validates language pack files and returns human-readable diagnostics.
    /// </summary>
    public static IReadOnlyList<string> ValidateLanguageFiles(string directory)
    {
        var lines = new List<string>();
        if (!Directory.Exists(directory))
            return [$"Missing language directory: {directory}"];

        var expected = English.Keys.OrderBy(key => key, StringComparer.OrdinalIgnoreCase).ToArray();
        foreach (string file in Directory.EnumerateFiles(directory, "*.json").OrderBy(path => path, StringComparer.OrdinalIgnoreCase))
        {
            try
            {
                var pack = JsonSerializer.Deserialize<LanguagePack>(File.ReadAllText(file), JsonOptions);
                if (pack is null)
                {
                    lines.Add($"FAIL {Path.GetFileName(file)}: empty or invalid JSON");
                    continue;
                }

                var missingKeys = expected.Where(key => !pack.Entries.ContainsKey(key)).ToArray();
                int missing = missingKeys.Length;
                int extra = pack.Entries.Keys.Count(key => !English.ContainsKey(key));
                string missingText = missing == 0 ? string.Empty : $" missingKeys={string.Join(",", missingKeys.Take(12))}";
                lines.Add($"{(missing == 0 ? "OK" : "WARN")} {Path.GetFileName(file)} language={pack.Language} entries={pack.Entries.Count} missing={missing} extra={extra}{missingText}");
            }
            catch (Exception ex)
            {
                lines.Add($"FAIL {Path.GetFileName(file)}: {ex.GetType().Name}: {ex.Message}");
            }
        }

        return lines.Count == 0 ? ["No language JSON files found."] : lines;
    }

    private static bool MergeMissingLanguageEntries(string path, LanguagePack builtInPack)
    {
        try
        {
            var existing = JsonSerializer.Deserialize<LanguagePack>(File.ReadAllText(path), JsonOptions);
            if (existing is null)
                return false;

            bool changed = false;
            var entries = new Dictionary<string, string>(existing.Entries, StringComparer.Ordinal);
            foreach (var (key, value) in builtInPack.Entries)
            {
                if (entries.ContainsKey(key))
                    continue;

                entries[key] = value;
                changed = true;
            }

            if (!changed)
                return true;

            var merged = new LanguagePack(
                string.IsNullOrWhiteSpace(existing.Language) ? builtInPack.Language : existing.Language,
                string.IsNullOrWhiteSpace(existing.NativeName) ? builtInPack.NativeName : existing.NativeName,
                entries);
            File.WriteAllText(path, JsonSerializer.Serialize(merged, JsonOptions));
            return true;
        }
        catch
        {
            return false;
        }
    }

    private static string LanguageDirectory => Path.Combine(AppContext.BaseDirectory, "lang");

    private static JsonSerializerOptions JsonOptions { get; } = new()
    {
        WriteIndented = true,
        PropertyNamingPolicy = JsonNamingPolicy.CamelCase,
        ReadCommentHandling = JsonCommentHandling.Skip,
        AllowTrailingCommas = true
    };

    private static Dictionary<string, Dictionary<string, string>> LoadExternalTranslations()
    {
        var result = new Dictionary<string, Dictionary<string, string>>(StringComparer.OrdinalIgnoreCase);
        string directory = LanguageDirectory;
        if (!Directory.Exists(directory))
            return result;

        foreach (string file in Directory.EnumerateFiles(directory, "*.json"))
        {
            try
            {
                var pack = JsonSerializer.Deserialize<LanguagePack>(File.ReadAllText(file), JsonOptions);
                if (pack?.Entries is { Count: > 0 } && !string.IsNullOrWhiteSpace(pack.Language))
                    result[NormalizeLanguage(pack.Language)] = new Dictionary<string, string>(pack.Entries, StringComparer.OrdinalIgnoreCase);
            }
            catch
            {
                // Bad custom translations must not stop the DAW from launching.
            }
        }

        return result;
    }

